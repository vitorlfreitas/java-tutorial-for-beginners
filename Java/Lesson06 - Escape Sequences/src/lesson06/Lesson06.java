/* Escape Sequences */

// Sometimes we want to use special characteres, so how to use them?
package lesson06;

public class Lesson06 {

    public static void main(String[] args) {
        // How to use "" in a string?
        System.out.println("Hello \"Mosh\""); // Hello "Mosh"
        // How to use backslash?
        System.out.println("c:\\Win\\.."); // c:\Win\..
        // How to break a line?
        System.out.println("Hello, \n My name is...");
        /* Hello, 
            My name is... */
        System.out.println("Hello,\tMy name.."); 
                            // Hello,	My name..   
    }
}
